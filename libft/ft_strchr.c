#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)(c & 0xff))
			return ((char *) s);
		s++;
	}
	if ((char)(c & 0xff) == 0)
		return ((char *) s);
	return (NULL);
}
